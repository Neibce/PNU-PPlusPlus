package com.pnuppp.pplusplus;

public class SubjectInfo {
    String subjectName;
    int credit;
    float grade;
    boolean isMajor;

    SubjectInfo(String subjectName, int credit, float grade, boolean isMajor){
        this.subjectName = subjectName;
        this.credit = credit;
        this.grade = grade;
        this.isMajor = isMajor;
    }
}
